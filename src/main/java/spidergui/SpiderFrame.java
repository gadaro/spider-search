    /**
     * Método que carga, al iniciar, las preferencias de usuario. En Windows se almacenan
     * en el registro en HKEY_LOCAL_MACHINE\Software\JavaSoft\Prefs\Nombre-de-clase.
     * Configura a partir de él la propiedad iteraciones y la carga en la pestaña
     * de configuración.
     */
    private void loadLastPreferences () {
        if (configPref != null) {
            // Obtiene las preferencias guardadas, en caso contrario establece un valor por defecto
            guiConfigDepth.setText(configPref.get(CFG_001, "5"));
            guiConfigPath.setText(configPref.get(CFG_002, ""));
            System.out.println("Cargadas preferencias de usuario de: " + configPref.absolutePath());
        } else {
            JOptionPane.showMessageDialog(null, "Error al crear clave de preferencias, "
                    + "no se guardará la configuración", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    /**
     * Método que guarda los valores de configuración en la clave 
     * HKEY_LOCAL_MACHINE\Software\JavaSoft\Prefs\Nombre-de-clase (En Windows).
     */
    private void saveConfigProperties () {
        if (configPref != null) {
            configPref.put(CFG_001, guiConfigDepth.getText());
            configPref.put(CFG_002, guiConfigPath.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Error al crear clave de preferencias, "
                    + "no se guardará la configuración", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }

