    private void guiMainStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guiMainStartActionPerformed
        if ( ( guiConfigDepth.getText().length() > 0 ) && ( getGuiConfigPath().getText().length() > 0 ) ) {
            //System.out.println(guiConfigDepth.getText());
            SpiderLogic logic = new SpiderLogic();
            logic.execute(Integer.parseInt(guiConfigDepth.getText()));
        } else {
            JOptionPane.showMessageDialog(null, "Asegúrese de que todos los campos "
                    + "están informados en la pestaña de Configuración", "Error", JOptionPane.ERROR_MESSAGE);
            guiMainStart.setEnabled(false);
        }
    }//GEN-LAST:event_guiMainStartActionPerformed

